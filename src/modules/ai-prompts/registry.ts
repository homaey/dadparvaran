const CATEGORY_LABELS: Record<PromptCategory, string> = {
  article: "تولید مقاله",
  calendar: "تقویم محتوا",
  review: "بازبینی کیفیت",
  rewrite: "بازنویسی",
  forms: "اوراق قضایی",
};

export function getDefaultPrompt(key: string): string {
  return DEFAULTS[key]?.value ?? "";
}

export function getAllPromptEntries(): PromptEntry[] {
  return Object.entries(DEFAULTS).map(([key, def]) => ({
    key,
    label: def.label,
    category: def.category,
    categoryLabel: CATEGORY_LABELS[def.category],
    defaultValue: def.value,
  }));
}

export async function getPrompt(key: string): Promise<string> {
  const row = await db.systemSetting.findUnique({ where: { key: `sysprompt_${key}` } });
  return row?.value ?? DEFAULTS[key]?.value ?? "";
}

export async function getAllPrompts(): Promise<{ prompts: Record<string, string>; entries: PromptEntry[] }> {
  const entries = getAllPromptEntries();
  const keys = entries.map((e) => `sysprompt_${e.key}`);
  const rows = await db.systemSetting.findMany({ where: { key: { in: keys } } });
  const dbMap: Record<string, string> = {};
  for (const r of rows) {
    dbMap[r.key.replace("sysprompt_", "")] = r.value;
  }
  const prompts: Record<string, string> = {};
  for (const e of entries) {
    prompts[e.key] = dbMap[e.key] ?? e.defaultValue;
  }
  return { prompts, entries };
}

export async function setPrompt(key: string, value: string) {
  if (!DEFAULTS[key]) return;
  await db.systemSetting.upsert({
    where: { key: `sysprompt_${key}` },
    update: { value },
    create: { key: `sysprompt_${key}`, value },
  });
}
