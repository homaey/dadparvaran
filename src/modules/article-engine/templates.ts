export function getTemplate(key: string): BlockDefinition[] {
  const t = templates[key as TemplateKey];
  if (!t) throw new Error(`قالب «${key}» معتبر نیست`);
  return t;
}

export function isHumanOnlyBlock(templateKey: string, blockKey: string) {
  return Boolean(templates[templateKey as TemplateKey]?.find((b) => b.key === blockKey)?.humanOnly);
}
