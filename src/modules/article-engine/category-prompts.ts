const ARTICLE_TYPE_KEYS = Object.values(ArticleType);

export async function getCategoryPrompt(articleType: string): Promise<string> {
  // رکوردهای پیش از تغییر تاکسونومی هنوز نوع قدیمی دارند؛ به پرامپت جانشین می‌رسند.
  const type = resolveArticleType(articleType);
  const row = await db.systemSetting.findUnique({ where: { key: `prompt_${type}` } });
  return row?.value || DEFAULT_CATEGORY_PROMPTS[type] || "";
}

export async function getAllCategoryPrompts(): Promise<{ prompts: Record<string, string>; defaults: Record<string, string> }> {
  const rows = await db.systemSetting.findMany({
    where: { key: { in: ARTICLE_TYPE_KEYS.map((t) => `prompt_${t}`) } },
  });
  const dbMap: Record<string, string> = {};
  for (const r of rows) {
    const type = r.key.replace("prompt_", "");
    dbMap[type] = r.value;
  }
  const prompts: Record<string, string> = {};
  for (const t of ARTICLE_TYPE_KEYS) {
    prompts[t] = dbMap[t] || DEFAULT_CATEGORY_PROMPTS[t] || "";
  }
  return { prompts, defaults: { ...DEFAULT_CATEGORY_PROMPTS } };
}

export async function setCategoryPrompt(articleType: string, value: string) {
  if (!(articleType in ArticleType)) return;
  await db.systemSetting.upsert({
    where: { key: `prompt_${articleType}` },
    update: { value },
    create: { key: `prompt_${articleType}`, value },
  });
}
